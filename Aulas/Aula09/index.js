let num1 = 2;
let num2 = 3;

const somar = (n1, n2) => {
  return n1 + n2;
}
const subtrair = (n1, n2) => {
  return n1 - n2;
}
const multiplicar = (n1, n2) => {
  return n1 * n2;
}
const dividir = (n1, n2) => {
  return n1 / n2;
}

console.log(somar(num1, num2));
console.log(subtrair(num1, num2));
console.log(multiplicar(num1, num2));
console.log(dividir(num1, num2));
