// Faça um algoritmo que tenha funções para cada operação aritimética básica.
function somar(num1, num2) {
  return num1 + num2;
}

function subtrair(num1, num2) {
  return num1 - num2;
}

function multiplicar(num1, num2) {
  return num1 * num2;
}

function dividir(num1, num2) {
  return num1 / num2;
}

console.log(somar(5, 2)) // 5 + 2 => retorna 7
console.log(subtrair(5, 2)) // 5 - 2 => retorna 3
console.log(multiplicar(5, 2)) // 5 * 2 => retorna 10
console.log(dividir(5, 2)) // 5 / 2 => retorna 2.5
