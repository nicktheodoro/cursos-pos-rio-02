let numero = 3;

// Exemplo incremento Maneira tradicional
numero = numero + 1;
// Exemplo decremento Maneira tradicional
numero = numero - 1;

// Exemplo incremento Simplificada tradicional
numero += 1;
// Exemplo decremento Simplificada tradicional
numero -= 1;

// Operadores de incremento/decremento
// Exemplo incremento de + 1
numero++;
// Exemplo decremento de - 1
numero--;
